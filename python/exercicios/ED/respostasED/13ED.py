#!/usr/bin/python
#-*- coding: utf-8 -*-

n = int(input("Digite um número: "))

if(n == 1):
	print("1-Domingo")
elif(n == 2):
	print("2-Segunda-feira")
elif(n == 3):
	print("3-Terça-feira")
elif(n == 4):
	print("4-Quarta-feira")
elif(n == 5):
	print("5-Quinta-feira")
elif(n == 6):
	print("6-Sexta-feira")
elif(n == 7):
	print("7-Sabádo")
else:
	print("valor inválido")
